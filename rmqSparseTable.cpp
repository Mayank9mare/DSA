//KnightMareVoid
const int MAXN=500;
int st[MAXN][MAXN];

void buildSparse(int a[],int n){
    int k=log2(MAXN)+1;
    for(int i=0;i<n;i++){
        st[i][0]=a[i];
    }
    for(int j=1;j<=k;j++){
        for(int i=0;i+(1<<j)<=n;i++){
            st[i][j]=min(st[i][j-1],st[i+(1<<(j-1))][j-1]);

        }
    }
    
}
int query(int l ,int r){
    int j=log2(r-l+1);
    int minimum=min(st[l][j],st[r-(1<<j)+1][j]);
    return minimum;
}
int example(){
    int a[] = { 7, 2, 3, 0, 5, 10, 3, 12, 128,222,333,33 }; 
    int n = sizeof(a) / sizeof(a[0]); 
    buildSparse(a, n); 
    cout << query(8, 11) << endl; 
    cout << query(4, 7) << endl; 
    cout << query(7, 8) << endl;
    return 0;
}


