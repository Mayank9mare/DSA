Mat mul(Mat a, Mat b) {
    Mat res = empty;
    for (int i = 0; i < K; ++i)
        for (int j = 0; j < K; ++j)
            for (int k = 0; k < K; ++k)
                res[i][k] = (res[i][k] + (ll)a[i][j] * b[j][k]) % mod;
    return res;
}


