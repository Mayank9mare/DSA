//KnightMareVoid
int n;
vector<vector<int>> adj;
vector<bool> visited;
vector<int> ans;
void dfs(int v){
    visited[v]=true;
    for(int u:adj[v]){
        if(!visited[u]){
            dfs(u);
        }
    }
    ans.pb(v);

}
void topological_sort(){
    visited.assign(n,false);
    ans.clear();
    for(int i=0;i<n;i++){
        if(!visited[i]){
            dfs(i);
        }
    }
    reverse(all(ans));
}
void t_sort(){
    vector<int> in_deg(n,0);
    for(int u=0;u<n;u++){
        for(auto x:adj[u]){
            in_deg[x]++;
        }
    }
    queue<int> q;
    for(int i=0;i<n;i++){
        if(in_deg[i]==0){
            q.push(i);
        }
    }
    int cnt=0;
    vector<int> top_order;
    while(!q.empty()){
        int u=q.front();
        q.pop();
        top_order.pb(u);
        for(auto x:adj[u]){
            if(--in_deg[x]==0){
                q.push(x);
            }
        }
        cnt++;
    }
    if(!cnt!=n){
        cout<<"No topological order"<<endl;
        return;
    }
    for(int i=0;i<top_order.size();i++){
        cout<<top_order[i]<<" ";
    }
    cout<<endl;

}



